// Switch 

const month = "march"

switch (month) {
    case "jan":
        console.log("january")
        break;
    case "feb":
        console.log("February")
        break;
    case "march":
        console.log("March")
        break;
    case "april":
        console.log("April")
        break;

    default:
        console.log("Default case matched ")
        break;
}